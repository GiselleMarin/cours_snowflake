-- Accès à la DB et aux schémas
GRANT USAGE ON DATABASE HEALT_APP_PROJET          TO ROLE dev_ops_role;
GRANT USAGE ON SCHEMA HEALT_APP_PROJET.RAW        TO ROLE dev_ops_role;
GRANT USAGE ON SCHEMA HEALT_APP_PROJET.STAGING    TO ROLE dev_ops_role;
GRANT USAGE ON SCHEMA HEALT_APP_PROJET.COMMON     TO ROLE dev_ops_role;

-- Droits DDL sur chaque schéma
GRANT CREATE TABLE, CREATE VIEW, CREATE PROCEDURE, CREATE FUNCTION, CREATE FILE FORMAT, CREATE STREAM, CREATE SEQUENCE
    ON SCHEMA HEALT_APP_PROJET.RAW     TO ROLE dev_ops_role;

GRANT CREATE TABLE, CREATE VIEW, CREATE PROCEDURE, CREATE FUNCTION, CREATE FILE FORMAT, CREATE STREAM, CREATE SEQUENCE
    ON SCHEMA HEALT_APP_PROJET.STAGING TO ROLE dev_ops_role;

GRANT CREATE TABLE, CREATE VIEW, CREATE PROCEDURE, CREATE FUNCTION, CREATE FILE FORMAT, CREATE STREAM, CREATE SEQUENCE
    ON SCHEMA HEALT_APP_PROJET.COMMON  TO ROLE dev_ops_role;

GRANT CREATE STAGE ON ALL SCHEMAS IN DATABASE HEALT_APP_PROJET    TO ROLE dev_ops_role;
GRANT CREATE STAGE ON FUTURE SCHEMAS IN DATABASE HEALT_APP_PROJET TO ROLE dev_ops_role;

GRANT CREATE PIPE ON ALL SCHEMAS IN DATABASE HEALT_APP_PROJET    TO ROLE dev_ops_role;
GRANT CREATE PIPE ON FUTURE SCHEMAS IN DATABASE HEALT_APP_PROJET TO ROLE dev_ops_role;

-- Warehouse pour exécuter les DDL interactivement
GRANT USAGE ON WAREHOUSE MY_WH TO ROLE dev_ops_role;

