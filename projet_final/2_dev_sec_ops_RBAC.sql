-- DevOps — Créer des objets (tables, procédures, file formats)

USE ROLE dev_sec_ops_role;

CREATE DATABASE IF NOT EXISTS  HEALT_APP_PROJET;
CREATE OR REPLACE WAREHOUSE my_wh WITH WAREHOUSE_SIZE='X-SMALL' INITIALLY_SUSPENDED=TRUE;


GRANT USAGE ON DATABASE HEALT_APP_PROJET TO ROLE dev_sec_ops_role;
GRANT CREATE SCHEMA ON DATABASE HEALT_APP_PROJET TO ROLE dev_sec_ops_role;
CREATE SCHEMA IF NOT EXISTS HEALT_APP_PROJET.RAW;
CREATE SCHEMA IF NOT EXISTS HEALT_APP_PROJET.COMMON;
CREATE SCHEMA IF NOT EXISTS HEALT_APP_PROJET.STAGING;
